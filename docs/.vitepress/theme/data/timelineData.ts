/**
 * 内容单元格
 * @param heading 单元格标题
 * @param items 单元格内的内容
 * @param isEmpty 是否是空内容
 */
export interface TimelineCell {
  heading: string // 单元格标题
  items: string[] // 单元格内的内容
  isEmpty: boolean // 是否是空内容
}

/**
 * 国家单元格
 * @param nation 国家名
 * @param colSpan 具有的子列数量
 */
export interface NationColumn {
  nation: string // 国家名
  colSpan: number // 具有的子列数量
}

/**
 * 时间段视域下的具体时间节点
 * @param subPeroid 时间节点名
 * @param cells 具体内容
 * @see TimelineCell
 */
export interface TimelineRow {
  subPeriod: string
  cells: TimelineCell[]
}

/**
 * 时间段组织
 * @param name 时间段名称
 * @param rows 时间节点组
 */
export interface TimelinePeriod {
  name: string
  rows: TimelineRow[]
}

